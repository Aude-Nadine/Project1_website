      <p>
        The bottom line on exercise
        Exercise and physical activity are great ways to feel better, boost your health and have fun. For most healthy adults, the U.S.
        Department of Health and Human Services recommends these exercise guidelines:
        Aerobic activity. Get at least 150 minutes of moderate aerobic activity or 75 minutes of vigorous aerobic activity a week, or a
        combination of moderate and vigorous activity. The guidelines suggest that you spread out this exercise during the course of a week.
        To provide even greater health benefit and to assist with weight loss or maintaining weight loss, at least 300 minutes a week is recommended.
        But even small amounts of physical activity are helpful. Being active for short periods of time throughout the day can add up to provide health benefit.
        Strength training. Do strength training exercises for all major muscle groups at least two times a week. Aim to do a single set of each exercise using a
        weight or resistance level heavy enough to tire your muscles after about 12 to 15 repetitions.
        Moderate aerobic exercise includes activities such as brisk walking, biking, swimming and mowing the lawn.
        Vigorous aerobic exercise includes activities such as running, heavy yardwork and aerobic dancing. Strength training can include use of weight machines,
        your own body weight, heavy bags, resistance tubing or resistance paddles in the water, or activities such as rock climbing.
        If you want to lose weight, meet specific fitness goals or get even more benefits, you may need to ramp up your moderate aerobic activity even more.
        Remember to check with your doctor before starting a new exercise program, especially if you have any concerns about your fitness, haven't exercised for a long time,
        have chronic health problems, such as heart disease, diabetes or arthritis.
